

<h1 id="font">Välkommen, hederlig frände, till vår borgs majestätiska hallar och salar. Som en strålande morgonstjärna lyser du på denna dag, och vi känner oss djupt beledsagade av din närvaro. Här, i denna tid av hjältar och ädla uppgifter, har du funnit din väg till riddarens sal och den ära som den innebär.
  Låt mig hälsa dig med en klingande fanfar och riddarnas löfte om beskydd och trofasthet. Som värd i denna borg, är det min plikt att hedra din ankomst och att välkomna dig med stolthet och glädje. Må vår tid tillsammans bli en minnesvärd episod i vårt gemensamma äventyr, och må du alltid finna vänskap och mod här i våra heliga väggar. För i denna borgs gemenskap, riddare, är du lika med en broder, en bundsförvant, och en hjälte som är värd all den ära som Medeltiden kan erbjuda.</h1>

